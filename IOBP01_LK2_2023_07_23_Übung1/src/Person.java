public class Person {
	private String name;
	private String vorname;
	
	public void setName (String n) {
		name = n;
	}
	
	public String getName () {
		return name;
	}
	
	public void setVorname (String v) {
		vorname = v;
	}
	
	public String getVorname () {
		return vorname;
	}
}
